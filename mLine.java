class mLine 
{
	public static void main(String[] args) 
	{ for (int i=0; i<31  ;i++ )
	{
		System.out.print("-");
	}
	System.out.println(" ");
	for (int i=0; i<16  ;i++ )
	{
		System.out.print("=");
	}
	System.out.println(" ");
	for (int i=0; i<51  ;i++ )
	{
		System.out.print("*");
	}
		System.out.println(" ");
	}
}
